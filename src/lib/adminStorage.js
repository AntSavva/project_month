const normalizeSiteData = (siteData) => {
  const productContent = siteData.productPage || createDefaultProductContent()
  const interiorContent = siteData.interiorPage || createDefaultInteriorContent()

  return {
    ...siteData,
    pages: siteData.pages.map((page) => ({
      ...page,
      content:
        page.content ||
        (page.type === 'product' && page.slug === 'service' ? productContent : undefined) ||
        (page.type === 'interior' && page.slug === 'interior' ? interiorContent : undefined) ||
        (page.type === 'product' ? createDefaultProductContent() : page.content) ||
        (page.type === 'interior' ? createDefaultInteriorContent() : page.content) ||
        (page.type === 'document' ? createDefaultDocumentContent() : page.content),
    })),
  }
}


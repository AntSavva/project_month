const data = JSON.parse(await readFile(siteDataPath, 'utf8'))
const now = new Date().toISOString()
const existingPages = data.pages || []

data.pages = [
  ...pages.map((page) => {
    const existing = existingPages.find((item) => item.id === page.id)
    return {
      ...(existing || {}),
      ...page,
      cover: existing?.cover || page.cover,
      createdAt: existing?.createdAt || now,
      updatedAt: now,
    }
  }),
  ...existingPages.filter((page) => !pages.some((item) => item.id === page.id)),
]

data.interiorPage = pages[0].content

await writeFile(siteDataPath, `${JSON.stringify(data, null, 2)}\n`, 'utf8')
console.log(`Seeded ${pages.length} interior pages`)
